/**
 * @file Subdomain Finder plugin
 * @module plugins/tool/subdomain
 * @description Discover subdomains for a given domain using subdomainfinder.in API
 * @license Apache-2.0
 */

import fetch from "node-fetch";

let handler = async (m, { text, usedPrefix, command }) => {
    if (!text) return m.reply(`Search for subdomains of a domain.\n\nExample: *${usedPrefix + command} windy.id*`);

    const domain = text.trim().toLowerCase().replace(/^https?:\/\//, "").split("/")[0];
    if (!/^[a-z0-9.-]+\.[a-z]{2,}$/.test(domain)) {
        return m.reply("❌ Invalid domain format. Example: *google.com*");
    }

    try {
        await m.reply(`🔍 *Searching subdomains for:* ${domain}...\nPlease wait a moment.`);

        const res = await fetch(`https://api.subdomainfinder.in/?domain=${domain}`, {
            headers: {
                "User-Agent": "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/120.0.0.0 Safari/537.36",
                "Referer": "https://subdomainfinder.in/"
            }
        });

        if (!res.ok) throw new Error(`API error: ${res.statusText}`);

        const json = await res.json();
        if (json.status !== "success" || !json.data || json.data.length === 0) {
            return m.reply(`❌ No subdomains found for *${domain}*.`);
        }

        const subdomains = json.data.map(item => typeof item === "string" ? item : item.subdomain);
        const total = json.count || subdomains.length;

        // WhatsApp message limit is ~4096 characters. Show first 50 results to be safe.
        const limit = 50;
        const list = subdomains.slice(0, limit).map((s, i) => `${i + 1}. ${s}`).join("\n");
        const more = total > limit ? `\n\n...and ${total - limit} others.` : "";

        const caption = `🌐 *SUBDOMAIN FINDER*\n\n` +
            `• *Domain:* ${domain}\n` +
            `• *Total:* ${total}\n\n` +
            `*Results (Top ${Math.min(limit, total)}):*\n` +
            list + more +
            `\n\n_Generated by Liora Bot_`;

        await m.reply(caption);
    } catch (e) {
        console.error("Subdomain Finder Error:", e);
        m.reply(`❌ Failed to search subdomains. Ensure the domain is correct or try again later.`);
    }
};

handler.help = ["subdofind", "findsubdo"];
handler.tags = ["tool"];
handler.command = /^(subdofind|findsubdo)$/i;

export default handler;
