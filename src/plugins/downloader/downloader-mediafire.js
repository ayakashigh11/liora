/**
 * @file Mediafire Downloader plugin
 * @module plugins/downloader/mediafire
 * @description Download files from Mediafire using NexRay API
 * @license Apache-2.0
 */

import fetch from "node-fetch";

let handler = async (m, { sock, text, usedPrefix, command }) => {
    if (!text) return m.reply(`Please provide a Mediafire URL.\n\nUsage: *${usedPrefix + command} (url)*\nExample: *${usedPrefix + command} https://www.mediafire.com/file/xxxxxx/file*`);

    const url = text.trim();
    if (!/mediafire\.com/i.test(url)) {
        return m.reply("❌ Invalid URL. Make sure it is a Mediafire link.");
    }

    try {
        await global.loading(m, sock);

        m.reply(`⏳ *Processing...* Fetching file information.`);

        const res = await fetch(`https://api.nexray.web.id/downloader/mediafire?url=${encodeURIComponent(url)}`);

        if (!res.ok) throw new Error(`NexRay API error: ${res.statusText}`);

        const json = await res.json();
        if (!json.status || !json.result) {
            return m.reply(`❌ Failed to fetch file information. Ensure the link is correct or try again later.`);
        }

        const { filename, filesize, mimetype, uploaded, download_url } = json.result;

        if (!download_url) {
            return m.reply("❌ No download link found for this file.");
        }

        const caption = `📁 *MEDIAFIRE DOWNLOADER*\n\n` +
            `• *Filename:* ${filename}\n` +
            `• *Size:* ${filesize}\n` +
            `• *Type:* ${mimetype}\n` +
            `• *Uploaded:* ${uploaded}\n\n` +
            `_Generated by Liora Bot_`;

        await sock.sendMessage(m.chat, {
            document: { url: download_url },
            caption: caption,
            fileName: filename,
            mimetype: mimetype
        }, { quoted: m });

    } catch (e) {
        global.logger.error({ error: e.message, url }, "Mediafire Downloader error");
        m.reply(`❌ An error occurred: ${e.message}`);
    } finally {
        await global.loading(m, sock, true);
    }
};

handler.help = ["mediafire"];
handler.tags = ["downloader"];
handler.command = /^(mediafire|mf)$/i;

export default handler;
