/**
 * @file GitHub Downloader plugin
 * @module plugins/downloader/gitclone
 * @description Download GitHub repositories as ZIP files using NexRay API
 * @license Apache-2.0
 * @author ayakashigh11
 */

let handler = async (m, { sock, text, usedPrefix, command }) => {
    if (!text) {
        return m.reply(`🎬 *GITHUB DOWNLOADER*\n\nUsage: *${usedPrefix + command} [github-url]*\nExample: *${usedPrefix + command} https://github.com/ayakashigh11/liora*`);
    }

    const githubRegex = /github\.com\/([\w.-]+)\/([\w.-]+)(\/)?/i;
    const match = text.match(githubRegex);

    if (!match) {
        return m.reply("❌ Invalid GitHub URL. Please provide a valid repository link.");
    }

    try {
        await global.loading(m, sock);
        m.reply(`⏳ *Processing...* Cloning repository.`);

        const apiUrl = `https://api.nexray.web.id/downloader/github?url=${encodeURIComponent(text.trim())}`;
        const response = await fetch(apiUrl);

        if (!response.ok) {
            throw new Error(`API returned HTTP ${response.status}`);
        }

        const data = await response.json();

        if (!data.status || !data.result) {
            return m.reply("❌ Failed to fetch repository data. Make sure the repository exists and is public.");
        }

        const { filename, repo, branch, url } = data.result;

        const caption = `🎬 *GITHUB DOWNLOADER*\n\n` +
            `• *Repository:* ${repo}\n` +
            `• *Branch:* ${branch}\n` +
            `• *Filename:* ${filename}\n\n` +
            `_Generated by Liora Bot_`;

        await sock.sendMessage(m.chat, {
            document: { url: url },
            fileName: filename,
            mimetype: "application/zip",
            caption: caption
        }, { quoted: m });

    } catch (e) {
        global.logger.error({ error: e.message, stack: e.stack }, "GitHub Downloader Error");
        m.reply(`❌ An error occurred: ${e.message}`);
    } finally {
        await global.loading(m, sock, true);
    }
};

handler.help = ["git", "gitclone"];
handler.tags = ["downloader"];
handler.command = /^(git|gitclone)$/i;

export default handler;
