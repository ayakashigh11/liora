/**
 * @file Instagram Downloader plugin
 * @module plugins/downloader/instagram
 * @description Download media (images/videos) from Instagram using NexRay API
 * @license Apache-2.0
 */

import fetch from "node-fetch";

let handler = async (m, { sock, text, usedPrefix, command }) => {
    if (!text) return m.reply(`Please provide an Instagram URL.\n\nUsage: *${usedPrefix + command} (url)*\nExample: *${usedPrefix + command} https://www.instagram.com/p/xxxxxx*`);

    const url = text.trim();
    if (!/instagram\.com/i.test(url)) {
        return m.reply("❌ Invalid URL. Make sure it is an Instagram link.");
    }

    try {
        await global.loading(m, sock);

        m.reply(`⏳ *Processing...* Fetching media from Instagram.`);

        const res = await fetch(`https://api.nexray.web.id/downloader/v1/instagram?url=${encodeURIComponent(url)}`);

        if (!res.ok) throw new Error(`NexRay API error: ${res.statusText}`);

        const json = await res.json();
        if (!json.status || !json.result) {
            return m.reply(`❌ Failed to fetch media. Ensure the link is correct or try again later.`);
        }

        const { title, author, likes, media } = json.result;

        if (!media || media.length === 0) {
            return m.reply("❌ No media found in this Instagram post.");
        }

        const caption = `📸 *INSTAGRAM DOWNLOADER*\n\n` +
            `• *Author:* ${author}\n` +
            `• *Likes:* ${likes || "-"}\n` +
            `• *Title:* ${title ? (title.length > 200 ? title.substring(0, 200) + "..." : title) : "-"}\n\n` +
            `_Generated by Liora Bot_`;

        // If it's a single item, send it. If carousel, send all.
        for (const item of media) {
            if (item.type === "video") {
                await sock.sendMessage(m.chat, {
                    video: { url: item.url },
                    caption: caption,
                    mimetype: "video/mp4"
                }, { quoted: m });
            } else if (item.type === "image") {
                await sock.sendMessage(m.chat, {
                    image: { url: item.url },
                    caption: caption
                }, { quoted: m });
            }
        }

    } catch (e) {
        global.logger.error({ error: e.message, url }, "Instagram Downloader error");
        m.reply(`❌ An error occurred: ${e.message}`);
    } finally {
        await global.loading(m, sock, true);
    }
};

handler.help = ["instagram", "ig", "igdl"];
handler.tags = ["downloader"];
handler.command = /^(instagram|ig|igdl)$/i;

export default handler;
